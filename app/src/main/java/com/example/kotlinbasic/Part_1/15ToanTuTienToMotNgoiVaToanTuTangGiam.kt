package com.example.kotlinbasic.Part_1

fun main() {
    var a = 6
    var b = -7
    var c = true
    var d = 6
    var e = 9


    println(a.unaryPlus()) // tương đương +a
    println(b.unaryMinus()) // tương đương -b
    println(c.not()) //tương đương !c
    println(d.inc()) //tương đương ++d
    println(e.dec()) //tương đương --e
}