package com.example.kotlinbasic.Part_1

fun main() {
    var number = 10

    number += 5
    println("number  = $number")

    number -= 3
    println("number  = $number")

    number *= 4
    println("number  = $number")

    number /= 2
    println("number  = $number")

    number %= 7
    println("number  = $number")
}